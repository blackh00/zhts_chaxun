function checkOptions(n){
	if(n=='0'){
		$(".phone_left .anlian").hide();
		$("#crush").show();
	}
	if(n=='a'){
		$(".phone_left .anlian").hide();
		$("#crush_a").show();
	}
	if(n=='b'){
		$(".phone_left .anlian").hide();
		$("#crush_b").show();
	}
	if(n=='c'){
		$(".phone_left .anlian").hide();
		$("#crush_c").show();
	}
	if(n=='d'){
		$(".phone_left .anlian").hide();
		$("#crush_d").show();
	}
	if(n=='e'){
		$(".phone_left .anlian").hide();
		$("#crush_e").show();
	}
}